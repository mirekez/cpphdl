static int32_t C[10][10];

static void uart_putc(char c)
{
    *UART_BASE = (uint32_t)(uint8_t)c;
}

static void uart_puts(const char* s)
{
    while (*s) {
        uart_putc(*s++);
    }
}

static void uart_put_i32(int32_t value)
{
    char buf[12];
    uint32_t n;
    int i = 0;

    if (value < 0) {
        uart_putc('-');
        n = (uint32_t)(-(value + 1)) + 1u;
    }
    else {
        n = (uint32_t)value;
    }

    do {
        buf[i++] = (char)('0' + (n % 10u));
        n /= 10u;
    } while (n != 0u);

    while (i > 0) {
        uart_putc(buf[--i]);
    }
}

void run(void) __attribute__((noinline));
void run(void)
{
    for (int i = 0; i < 10; ++i) {
        for (int j = 0; j < 10; ++j) {
            int32_t acc = 0;
            for (int k = 0; k < 20; ++k) {
                acc += A[i][k] * B[k][j];
            }
            C[i][j] = acc;
        }
    }

    uart_puts("C=A(10x20)*B(20x10)\n");
    for (int i = 0; i < 10; ++i) {
        for (int j = 0; j < 10; ++j) {
            if (j != 0) {
                uart_putc(' ');
            }
            uart_put_i32(C[i][j]);
        }
        uart_putc('\n');
    }
}

void _start(void) __attribute__((naked, section(".init")));
void _start(void)
{
    __asm__ volatile(
        "li sp, 0x6f000\n"
        "call run\n"
        "1: j 1b\n"
    );
}
